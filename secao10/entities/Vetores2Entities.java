package entities;

public class Vetores2Entities {
	private String name;
	private double price;
	
//constructor
	public Vetores2Entities(String name, double price) {
		this.name = name;
		this.price = price;
	}
	
//get/set
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public double getPrice() {
		return price;
	}
	
	public void setPrice(double price) {
		this.price = price;
	}
	
//methods
	
}
